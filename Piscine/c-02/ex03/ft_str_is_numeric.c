int	ft_str_is_numeric(char *str)
{
	int		i;
	int		ret;

	i = 0;
	ret = 1;
	while (str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
		{
			ret = 0;
		}
		i++;
	}
	return (ret);
}
