int	ft_str_is_uppercase(char *str)
{
	int		i;
	int		ret;

	i = 0;
	ret = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 'A' || str[i] > 'Z')
		{
			ret = 0;
		}
		i++;
	}
	return (ret);
}
